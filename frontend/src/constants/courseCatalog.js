export const COURSE_OPTIONS = COURSE_GROUPS.flatMap((group) => group.courses);

export function getAllCourseOptions() {
  return COURSE_GROUPS;
}

export function getCourseOptions() {
  return COURSE_OPTIONS;
}

export function getBranchOptions(courseValue) {
  if (!courseValue) {
    return COURSE_OPTIONS.flatMap((course) => course.branches || []);
  }

  const course = COURSE_OPTIONS.find((item) => item.value === courseValue);
  return course?.branches || [];
}

export function getBranchLabel(courseValue, branchValue) {
  const branch = getBranchOptions(courseValue).find((item) => item.value === branchValue);
  return branch?.label || branchValue || 'NA';
}

export function getCourseLabel(courseValue) {
  const course = COURSE_OPTIONS.find((item) => item.value === courseValue);
  return course?.label || courseValue || 'NA';
}

function uniqueOptions(options) {
  const seen = new Set();
  return options.filter((option) => {
    const key = String(option.value || '').trim().toUpperCase();
    if (!key || seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function isLikelyRollNumber(value) {
  const normalized = String(value || '').trim().replace(/\s+/g, '').toUpperCase();
  if (!normalized) return false;
  return /^\d{2,4}[A-Z]{2,5}\d{2,5}$/.test(normalized);
}

export function mergeCourseOptions(facetValues = []) {
  const catalogOptions = COURSE_OPTIONS.map((course) => ({
    value: course.value,
    label: course.label
  }));

  const facetOptions = (facetValues || [])
    .filter(Boolean)
    .map((value) => ({
      value,
      label: getCourseLabel(value)
    }));

  return uniqueOptions([...catalogOptions, ...facetOptions]).sort((left, right) => left.label.localeCompare(right.label));
}

export function mergeBranchOptions(courseValue, facetValues = []) {
  const catalogOptions = getBranchOptions(courseValue).map((branch) => ({
    value: branch.value,
    label: branch.label
  }));

  const facetOptions = (facetValues || [])
    .filter((value) => value && !isLikelyRollNumber(value))
    .map((value) => ({
      value,
      label: getBranchLabel(courseValue, value)
    }));

  return uniqueOptions([...catalogOptions, ...facetOptions]).sort((left, right) => left.label.localeCompare(right.label));
}